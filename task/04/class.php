<?php
class mainClass
{
    public function penjumlahan($n1,$n2){
        return $n1 + $n2;
    }
    public function pengurangan($n1,$n2){
        return $n1 - $n2;
    }
    public function perkalian($n1,$n2){
        return $n1 * $n2;
    }
    public function pembagian($n1,$n2){
        return $n1 / $n2;
    }
}
?>